package com.movie.model;

public class PlaysModel {
    String id="";
    String halName="";
    String movName="";
    String plaStarttime="";
    String movTime="";
    String price="";
    String halId="";
    String movId="";


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHalName() {
        return halName;
    }

    public void setHalName(String halName) {
        this.halName = halName;
    }

    public String getMovName() {
        return movName;
    }

    public void setMovName(String movName) {
        this.movName = movName;
    }

    public String getPlaStarttime() {
        return plaStarttime;
    }

    public void setPlaStarttime(String plaStarttime) {
        this.plaStarttime = plaStarttime;
    }

    public String getMovTime() {
        return movTime;
    }

    public void setMovTime(String movTime) {
        this.movTime = movTime;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getHalId() {
        return halId;
    }

    public void setHalId(String halId) {
        this.halId = halId;
    }

    public String getMovId() {
        return movId;
    }

    public void setMovId(String movId) {
        this.movId = movId;
    }
}
